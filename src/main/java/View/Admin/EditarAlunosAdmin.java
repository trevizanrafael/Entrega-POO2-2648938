    private void itemConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemConsultarActionPerformed
        try {
            String cpf = cxCPF.getText().trim();   // só usa o CPF mesmo

            if (cpf.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Informe o CPF do aluno para consultar.",
                    "Aviso",
                    javax.swing.JOptionPane.WARNING_MESSAGE
                );
                return;
            }

            ConsultarAluno consulta = new ConsultarAluno();
            Aluno aluno = consulta.buscarPorCpf(cpf);

            if (aluno == null) {
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Aluno não encontrado.",
                    "Aviso",
                    javax.swing.JOptionPane.WARNING_MESSAGE
                );
                return;
            }

            // abre a janelinha SOMENTE LEITURA
            AlunoDialog dialog = new AlunoDialog(this, aluno, true);
            dialog.setVisible(true);
            // não chama getAluno(), não salva nada – é só visualização

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Erro ao consultar aluno:\n" + e.getMessage(),
                "Erro",
                javax.swing.JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_itemConsultarActionPerformed

    private void itemEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemEditarActionPerformed
        try {
            String cpf = cxCPF.getText().trim(); // JTextField CPF da tela principal

            if (cpf.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Informe o CPF do aluno que deseja editar.",
                    "Aviso",
                    javax.swing.JOptionPane.WARNING_MESSAGE
                );
                return;
            }

            // 1) busca aluno no banco
            ConsultarAluno consulta = new ConsultarAluno();
            Aluno existente = consulta.buscarPorCpf(cpf);

            if (existente == null) {
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Aluno não encontrado.",
                    "Aviso",
                    javax.swing.JOptionPane.WARNING_MESSAGE
                );
                return;
            }

            // 2) abre dialog já preenchido
            AlunoDialog dialog = new AlunoDialog(this, existente); // edição
            dialog.setVisible(true);
            if (!dialog.isConfirmado()) return;
            Aluno atualizado = dialog.getAluno();
            // garante que o CPF não foi mudado
            atualizado.setCpf(existente.getCpf());

            // 4) salva no banco
            new Control.CRUDAluno.EditarAluno().executar(atualizado);

            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Aluno atualizado com sucesso!",
                "Sucesso",
                javax.swing.JOptionPane.INFORMATION_MESSAGE
            );

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Erro ao editar aluno:\n" + e.getMessage(),
                "Erro",
                javax.swing.JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_itemEditarActionPerformed

