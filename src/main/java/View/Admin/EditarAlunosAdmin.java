    private void itemExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemExcluirActionPerformed
        try {
            String cpf = cxCPF.getText().trim();  // campo CPF da tela

            if (cpf.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Informe o CPF do aluno que deseja excluir.",
                    "Aviso",
                    javax.swing.JOptionPane.WARNING_MESSAGE
                );
                return;
            }

            int op = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Tem certeza que deseja excluir o aluno com CPF " + cpf + "?",
                "Confirmar exclusão",
                javax.swing.JOptionPane.YES_NO_OPTION
            );

            if (op != javax.swing.JOptionPane.YES_OPTION) {
                return; // usuário desistiu
            }

            Control.CRUDAluno.ApagarAluno apagar = new Control.CRUDAluno.ApagarAluno();
            int apagados = apagar.porCpf(cpf);

            if (apagados == 0) {
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Nenhum aluno encontrado com esse CPF.",
                    "Aviso",
                    javax.swing.JOptionPane.WARNING_MESSAGE
                );
            } else {
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Aluno excluído com sucesso!",
                    "Sucesso",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE
                );

                // limpa e destrava os campos
                cxCPF.setText("");
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(
                this,
                "Erro ao excluir aluno:\n" + e.getMessage(),
                "Erro",
                javax.swing.JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_itemExcluirActionPerformed

